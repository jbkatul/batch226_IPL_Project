package com.tka.Cacuator;

public class Caculator {

	public int Addtion(int a, int b) {
		int Sum=a+b;
		return Sum;
	}
	public int Substarion(int a, int b) {
		int Sub=a-b;
		return Sub;
	}
	public int Divison(int a, int b) {
		int Div=a/b;
		return Div;
	}
	public int Multipication(int a, int b) {
		int Mul=a*b;
		return Mul;
	}
}

	// Method to find max number in an array
	public int max(int[] a) {
		
		//  my self pradip shinde 
		
		int max = a[0];
		for (int i = 1; i < a.length; i++) {
			if (a[i] > max) {
				max = a[i];
			}
		}
		return max;
	}
